            try { 
                using (Mp3FileReader mp3 = new Mp3FileReader(from)) {
                    using (WaveStream pcm = WaveFormatConversionStream.CreatePcmStream(mp3)) {
                        WaveFileWriter.CreateWaveFile(to, pcm);
                    }
                }
